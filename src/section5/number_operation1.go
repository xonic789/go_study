package main

import "fmt"

func main() {
	var n1 uint8 = 125
	var n2 uint8 = 90

	fmt.Println("ex 1 : ", n1+n2)
	fmt.Println("ex 1 : ", n1-n2)
	fmt.Println("ex 1 : ", n1*n2)
	fmt.Println("ex 1 : ", n1/n2)
	fmt.Println("ex 1 : ", n1%n2)
	fmt.Println("ex 1 : ", n1<<n2)
	fmt.Println("ex 1 : ", n1>>n2)
	fmt.Println("ex 1 : ", ^n1)

}
